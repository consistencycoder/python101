# Try it yourself: 
# 2-2. Simple Messages: Assign a message to a variable, 
# and print that message. Then change the value of the variable 
# to a new message, and print the new message
# output: 
# This is the message I am assigning to the variable named message
# This is the NEW string I am assigning to message
# explanation: A variable named message is assigned an initial 
# string value, and that value is printed. Then, the variable is
# reassigned another string value, and that value is printed.
# This is an example of variable reassignment.

message = "This is the string I am assigning to the variable named message"
print(message)

# now I'm going to reassign the variable message then print it
message = "This is the NEW string I am assigning to message"
print(message)